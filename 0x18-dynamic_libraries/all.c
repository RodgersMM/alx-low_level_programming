#include <stdio.h>

/**
 * Add- add 2 intergers
 * @a: first int
 * @b: second int
 * Return: addition
 */

int add(int a, int b)
{
	return(a + b);
}

/**
 * sub- add 2 intergers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */

int sub(int a, int b)
{
	return(a - b);
}


/**
 * div- add 2 intergers
 * @a: first int
 * @b: second int
 * Return: division
 */

int div(int a, int b)
{
	return(a / b);
}

/**
 * mul- add 2 intergers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */

int mul(int a, int b)
{
	return(a * b);
}

